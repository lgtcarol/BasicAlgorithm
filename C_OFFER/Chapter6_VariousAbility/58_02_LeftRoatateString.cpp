// 面试题58（二）：左旋转字符串
// 题目：字符串的左旋转操作是把字符串前面的若干个字符转移到字符串的尾部。
// 请定义一个函数实现字符串左旋转操作的功能。比如输入字符串"abcdefg"和数
// 字2，该函数将返回左旋转2位得到的结果"cdefgab"。

#include <cstdio>
#include "Utilities\StringUtil.cpp"
#include <string.h>
/**
1. 其实和(一)是一样的，全部翻转，再局部翻转回来
    区别在于：本题只划分分两部分，上题依据空格在用while控制Reverse次数 
2.  长度内部的长度要判断长度的合理性，如本题的n和nLength 
3.   Attention:自己实现用c语言的字符串函数:strlen,strcmp等  
*/
char* LeftRotateString(char* pStr, int n)
{
    if(pStr != NULL)
    {
       // int nLength = static_cast<int>(strlen(pStr));
        int nLength = strlen(pStr);
        if(nLength > 0 && n > 0 && n < nLength)
        {
            char* pFirstStart = pStr;  
            char* pFirstEnd = pStr + n - 1;
            char* pSecondStart = pStr + n;
            char* pSecondEnd = pStr + nLength - 1;//lgt注意这是字符串，故通过长度控制位置时要加初始指针位置 

            // 翻转字符串的前面n个字符
            Reverse(pFirstStart, pFirstEnd);
            // 翻转字符串的后面部分
            Reverse(pSecondStart, pSecondEnd);
            // 翻转整个字符串
            Reverse(pFirstStart, pSecondEnd);
        }
    }

    return pStr;
}

// ====================测试代码====================
void Test(const char* testName, char* input, int num, const char* expectedResult)
{
    if(testName != NULL)
        printf("%s begins: ", testName);

    char* result = LeftRotateString(input, num);

    if((input == NULL && expectedResult == NULL)
        || (input != NULL && strcmp(result, expectedResult) == 0))
        printf("Passed.\n\n");
    else
        printf("Failed.\n\n");
}

